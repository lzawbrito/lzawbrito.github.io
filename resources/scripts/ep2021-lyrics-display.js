const $marinus = $('#marinus');
var marinusSelected = false;
const $mukilteoSpeedway = $('#mukilteo-speedway');
var mukilteoSpeedwaySelected = false;
const $spider = $('#spider');
var spiderSelected = false;
const $hypercommodity = $('#hypercommodity');
var hypercommoditySelected = false;


function resetUnderlines(){
    $marinus.css({ "text-decoration": "none" });
    $mukilteoSpeedway.css({ "text-decoration": "none" });
    $spider.css({ "text-decoration": "none" });
    $hypercommodity.css({ "text-decoration": "none" });
};

$marinus.mouseenter(function () {
    $marinus.css({ "text-decoration": "underline" });
});

$marinus.mouseleave(function () {
    if (!marinusSelected) {
        $marinus.css({ "text-decoration": "none" });
    }
});

$marinus.click(function () {
    marinusSelected = true;
    mukilteoSpeedwaySelected = false;
    hypercommoditySelected = false;
    spiderSelected = false;
    resetUnderlines();
    $marinus.css({ "text-decoration": "underline" });
    document.getElementById('lyrics-display').innerHTML = lyricsMarinus;
});


